int	ft_str_is_uppercase(char *str)
{
	int	flag;
	int	i;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 65 && str[i] <= 90))
		{
			flag = 0;
			return (flag);
		}
		++i;
	}
	return (flag);
}
